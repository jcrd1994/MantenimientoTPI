package com.tpimaven.tpimantenimiento;

/**
 *
 * @author LENOVO
 */
public class MPControlGeneralEconomia {
    
    String HistoricosMantenimiento;
    int NoInventario;
    String Marca;
    String NoSerie;
    String Modelo;
    String Responsable;
    String SistemaOperativo;
    String Version;
    boolean Licencia;
    String Observaciones;

    public MPControlGeneralEconomia() {
    }
    

    public String getHistoricosMantenimiento() {
        return HistoricosMantenimiento;
    }

    public void setHistoricosMantenimiento(String HistoricosMantenimiento) {
        this.HistoricosMantenimiento = HistoricosMantenimiento;
    }

    public int getNoInventario() {
        return NoInventario;
    }

    public void setNoInventario(int NoInventario) {
        this.NoInventario = NoInventario;
    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String Marca) {
        this.Marca = Marca;
    }

    public String getNoSerie() {
        return NoSerie;
    }

    public void setNoSerie(String NoSerie) {
        this.NoSerie = NoSerie;
    }

    public String getModelo() {
        return Modelo;
    }

    public void setModelo(String Modelo) {
        this.Modelo = Modelo;
    }

    public String getResponsable() {
        return Responsable;
    }

    public void setResponsable(String Responsable) {
        this.Responsable = Responsable;
    }

    public String getSistemaOperativo() {
        return SistemaOperativo;
    }

    public void setSistemaOperativo(String SistemaOperativo) {
        this.SistemaOperativo = SistemaOperativo;
    }

    public String getVersion() {
        return Version;
    }

    public void setVersion(String Version) {
        this.Version = Version;
    }

    public boolean isLicencia() {
        return Licencia;
    }

    public void setLicencia(boolean Licencia) {
        this.Licencia = Licencia;
    }

    public String getObservaciones() {
        return Observaciones;
    }

    public void setObservaciones(String Observaciones) {
        this.Observaciones = Observaciones;
    }
    
    
}
